package day3;

public class OperatorsDemo3 {

	public static void main(String[] args) {
		// 3. Increment/Decrement Operator
		// Post a++ a--
		// Pre ++a --a

		int i = 5;
		System.out.println(i);
		++i;// i += 1;// i = i+1;
		System.out.println(i);

		int a = 9;
		System.out.println(a);// 9
		System.out.println(a++);// 9
		System.out.println(a);// 10
	}

}
